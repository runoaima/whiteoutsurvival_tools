document.addEventListener("DOMContentLoaded", () => {
    const modal = document.getElementById("modal");
    const slots = document.querySelectorAll(".slot");
    let currentSlot = null;

    // モーダルを開く
    slots.forEach(slot => {
        slot.addEventListener("click", () => {
            currentSlot = slot;
            modal.classList.remove("hidden");
            modal.classList.add("active");
            document.body.style.overflow = "hidden"; // 背面スクロール禁止
        });
    });

    // モーダルを閉じる関数
    function closeModalFunc() {
        modal.classList.remove("active");
        modal.classList.add("hidden");
        document.body.style.overflow = ""; // 背面スクロール解除
    }

    // モーダル外クリック / タップで閉じる
    ["click", "touchstart"].forEach(evt => {
        modal.addEventListener(evt, e => {
            if (e.target === modal) closeModalFunc();
        });
    });

    // キャラ選択
    document.querySelectorAll(".character-card").forEach(card => {
        card.addEventListener("click", () => {
            if (!currentSlot) return;
            const img = card.querySelector("img")?.src;
            const name = card.dataset.name || "";

            currentSlot.innerHTML = (name === "none" || !img)
                ? `<p>キャラなし</p>`
                : `<img src="${img}" alt="${name}">`;

            closeModalFunc();
        });
    });
});



document.addEventListener("DOMContentLoaded", () => {
    const modal = document.getElementById("modal");
    const slots = document.querySelectorAll(".slot");
    const characterCards = document.querySelectorAll(".character-card");
    const skillDisplayContainer = document.getElementById("skillDisplayContainer");

    let currentSlotIndex = null;

    // 初期化：3枠分のスキルボックスを用意
    const skillBoxes = [];
    for (let i = 0; i < 3; i++) {
        const box = document.createElement("div");
        box.classList.add("skill-display");
        box.innerHTML = `<p class="empty-slot">🕳 未選択</p>`;
        skillDisplayContainer.appendChild(box);
        skillBoxes.push(box);
    }

    // スロットをクリックするとモーダルを開く
    slots.forEach((slot, index) => {
        slot.addEventListener("click", () => {
            currentSlotIndex = index;
            modal.classList.remove("hidden");
        });
    });

    // キャラ選択
    characterCards.forEach(card => {
        card.addEventListener("click", () => {
            if (currentSlotIndex === null) return;
            const name = card.dataset.name;

            // キャラなし
            if (name === "none") {
                skillBoxes[currentSlotIndex].innerHTML = `<p class="empty-slot">🕳 未選択</p>`;
                modal.classList.add("hidden");
                currentSlotIndex = null;
                return;
            }

            const hero = heroes[name];
            if (!hero) return;

            const skillMap = {
                [hero.expedition1_name]: hero.expedition1,
                [hero.expedition2_name]: hero.expedition2,
                [hero.expedition3_name]: hero.expedition3,
                [hero.expedition4_name]: hero.expedition4,
            };

            // 表作成
            let html = `
                <h5>${name}（攻撃:${hero.expedition_attack} / 防御:${hero.expedition_defense}）</h5>
                <div class="table-container">
                    <table class="skill-table">
                        <thead>
                            <tr><th>スキル</th><th>説明</th></tr>
                        </thead>
                        <tbody>
            `;
            for (let [key, value] of Object.entries(skillMap)) {
                if (value && value !== "") {
                    html += `<tr><td>${key}</td><td>${value}</td></tr>`;
                }
            }
            html += `</tbody></table></div>`;

            // スロットに対応した表示エリアを更新
            skillBoxes[currentSlotIndex].innerHTML = html;
            modal.classList.add("hidden");
            skillDisplayContainer.classList.remove("hidden");
            currentSlotIndex = null;
        });
    });
});
